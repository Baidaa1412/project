    public function addToCart(Request $request, $id)
    {
        if (Auth::check()) {
            $user = auth()->user();
            $product = Product::find($id);

            // Create a new Cart item and set its attributes
            $cart = new Cart();
            $cart->user_id = $user->id;
            $cart->product_id = $product->id;
            $cart->price = $product->DiscountPrice; // Assuming price is a property of the product
            $cart->quantity = 1; // You may adjust the quantity as needed
            $cart->total = $cart->price * $cart->quantity;
            $cart->status = 'pending';
            $cart->save();

            return redirect()->back()->with('message',' Product Added successfully ');
        } else {
            return redirect('login');
        }
    }

